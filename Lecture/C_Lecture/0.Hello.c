#include <stdio.h>

int main(void)
{
	printf("hello world\n");



	printf("I am daewoo\n");

	printf("%d\n", 10);
	printf("%lf\n", 3.4);
	printf("%.1lf\n", 3.145);
	printf("%.2f\n", 3.223455);
	printf("%lf\n", 1.e6);
	printf("%.7lf\n", 3.16e-6);
	printf("%c\n", 'a');
	printf("%s\n", 'B');


	return 0;
}

